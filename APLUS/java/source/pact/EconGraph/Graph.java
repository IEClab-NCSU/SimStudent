package pact.EconGraph;

import javax.swing.JApplet;


/////////////////////////////////////////////////////////////////////
//
//
/////////////////////////////////////////////////////////////////////
public class Graph extends JApplet implements Runnable  {

	GraphPanel graphPanel;
	Thread runner;
	
	/////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////
	public Graph() {
		graphPanel = new GraphPanel();
		getContentPane().add (graphPanel);
	}

	/////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////
	public void init () {
            graphPanel.setApplet (this);
            graphPanel.init();
	}
	

	/////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////
	public void start()
	{
	  if (runner == null)
	  {
	     runner = new Thread(this);
	     runner.start();
	  }
	}

	/////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////
	public void run()
	{
	  graphPanel.run();
	}

	/////////////////////////////////////////////////////////////////////
	//
	//
	/////////////////////////////////////////////////////////////////////
	public void stop()
	{
	  if (runner != null)
	  {
	     runner.stop();
	     runner = null;
	  }
	}
}